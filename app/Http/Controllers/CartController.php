    {

        $cart = new Cart();


        $cart->userid = $request->input('id');
        $cart->category = $request->input('category');

        $cart->quantity = $request->input('quantity');


        $cart->save();

        return redirect()->back()->with('success', 'Prekė pridėta');
    }

